

import java.util.Scanner;

public class helloFor {
	
	public static void main(String[] args) {

		
	int i;
	
	System.out.println("Pengulangan Menaik (Increment) ");
//	Pengulangan Menaik
//	for i=1 to 10 do   { ulangi sebanyak 10 kali }
//	 write(’Hello, world’)
//	  endfor
		for(i=1;i<=10;i++) {
			System.out.println("Hello World");
		}
		
		
		
		
		System.out.println("Pengulangan Menurun (Decrement) ");
//	Pengulangan Menurun
//		for i=10 downto 1 do   { ulangi sebanyak 10 kali }
//		 write(’Hello, world’)
//		  endfor
		for(i=10;i>=1;i--) {
			System.out.println("Hello World");
		}
		

		
	}


}
